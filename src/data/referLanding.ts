/*
  Данные лендинга /refer/. Всё, что можно итерировать
  (стат-блоки, кейсы, FAQ, шаги и т.д.) хранится массивами —
  меняешь массив, страница пересобирается автоматически.

  Поля с rich-разметкой («title», «headline») — HTML-строки,
  где допустима подсветка через .text-acid / .text-dim /
  .text-fuchsia2 / .ul-a / .ul-f / .bg-acid и пр.
*/

export interface RichString {
  /** HTML-фрагмент. Отрисовывается через set:html. */
  html: string;
}

/** Метка-подсветка для <EgeH2>. */
export interface H2Mark {
  text: string;
  underline?: "acid" | "fuchsia";
  color?: "acid" | "fuchsia" | "dim" | "paper" | "ink";
}

/** Заголовок раздела для <EgeH2>: полный текст + подсвечиваемые куски. */
export interface SectionTitle {
  text: string;
  marks?: H2Mark[];
}

