/**
 * Метаданные категорий Академии: символы для «элементных» плашек
 * и (опционально) кураторский SEO-контент. Если для категории нет
 * записи в CAT_META — описание и FAQ генерируются по шаблону из реальных данных.
 */
export const CAT_SYM: Record<string, string> = {
  "Школьникам": "Шк",
  "Новости": "Нв",
  "Родителям": "Рд",
  "Задачи в химии": "34",
  "Экзамен ЕГЭ": "ЕГ",
  "Поступление в ВУЗ": "Вз",
  "Органическая химия": "Ог",
  "Задания из ЕГЭ по химии": "Зэ",
  "Неорганическая химия": "Нг",
  "Общая химия": "Об",
};

export const catSym = (name: string): string =>
  CAT_SYM[name] ?? (name.replace(/[^А-Яа-яA-Za-z]/g, "").slice(0, 2) || "—");

export interface CatMeta {
  introHtml?: string; // переопределяет шаблонное описание
  faq?: { q: string; a: string }[]; // переопределяет шаблонный FAQ
}

// Кураторские переопределения по slug категории (заполняется по мере надобности).
export const CAT_META: Record<string, CatMeta> = {};
